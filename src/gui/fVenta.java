    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        boolean encontrado = false;
        for (int i = 0; i < lista.size(); i++) {
            if (lista.get(i).getId() == productos[cmbProductos.getSelectedIndex()].getId()){
                lista.set(i, new listaVenta(productos[cmbProductos.getSelectedIndex()].getId(), Integer.parseInt(cmbCantidad.getSelectedItem().toString())));
                encontrado = true;
            }
        }
        if (!encontrado) {
            lista.add(new listaVenta(productos[cmbProductos.getSelectedIndex()].getId(), Integer.parseInt(cmbCantidad.getSelectedItem().toString())));
        }
        cargarTabla();
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int c = tblProductos.getSelectedColumn();
        if (c >= 0) {
            for (int i = 0; i < lista.size(); i++) {
                if (lista.get(i).getId() == Integer.parseInt(String.valueOf(tblProductos.getValueAt(c, 0)))) {
                    lista.remove(i);
                }
            }
            cargarTabla();
        }
        else{
            JOptionPane.showMessageDialog(this, "Seleccione un producto de la tabla");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

