    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String[] filtros = {"Id", "Producto", "Descripcion"};
        try {
            filtros = new fBuscar(this).buscar(filtros);
            try {
                Float.parseFloat(filtros[1]);
                mostrar(filtros[0] + " LIKE " + filtros[1]);
            } catch (Exception e) {
                mostrar(filtros[0] + " LIKE '%" + filtros[1] + "%'");
            }
        } catch (Exception e) {
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (tblData.getSelectedRow() >= 0) {
            if (JOptionPane.showOptionDialog(null, "Â¿Seguro que desea eliminar?", "", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, new Object[]{"Si", "No"}, "No") == JOptionPane.YES_OPTION) {
                if (logica.eliminar(tblData.getValueAt(tblData.getSelectedRow(), 0).toString())) {
                    mostrar("");
                }
                else{
                    JOptionPane.showMessageDialog(this, "No se pudo guardar los datos");
                }
            }
            
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un dato valido en la tabla");
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (tblData.getSelectedRow() >= 0) {
            String[] datos = new String[cantDatos];
            for (int i = 0; i < cantDatos; i++) {
                datos[i] = tblData.getValueAt(tblData.getSelectedRow(), i).toString();
            }
            mostrar(Integer.parseInt(datos[0]), Float.parseFloat(datos[1]), datos[5], datos[2], datos[3], datos[4], datos[6]);
        }
        else{
            JOptionPane.showMessageDialog(this, "Seleccione un dato valido en la tabla");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (logica.editar(Integer.parseInt(txtId.getText()), Float.parseFloat(String.valueOf(spnMonto.getValue())), txtProductos.getText(), cmbMetodoPago.getSelectedItem().toString())) {
            pnl.setSelectedIndex(0);
        }
        else{
            JOptionPane.showMessageDialog(this, "No se pudo guardar los datos");
        }
        mostrar("");
    }//GEN-LAST:event_btnGuardarActionPerformed

