    void mostrar(String filtro){
        btnIndex.setVisible(!filtro.equals(""));
        for (int i = 0; i < 100; i++) {
            for (int j = 0; j < cantDatos; j++) {
                tblData.setValueAt(null, i, j);
            }
        }
        venta[] datos = logica.seleccionar(filtro);
        try {
            
            for (int i = 0; i < datos.length; i++) {
                tblData.setValueAt(datos[i].getId(), i, 0);
                tblData.setValueAt(datos[i].getMonto(), i, 1);
                tblData.setValueAt(datos[i].getMetodoPago(), i, 2);
                tblData.setValueAt(datos[i].getComprador(), i, 3);
                tblData.setValueAt(datos[i].getVendedor(), i, 4);
                tblData.setValueAt(datos[i].getProductos(), i, 5);
                tblData.setValueAt(datos[i].getFecha(), i, 6);
            }
        } catch (Exception e) {
        }
    }
    
    void mostrar(int id, float monto, String productos, String metodoPago, String comprador, String vendedor, String fecha){
        txtId.setText(String.valueOf(id));
        spnMonto.setValue(Float.valueOf(String.valueOf(monto)));
        if (metodoPago.equals("Efectivo")) {
            cmbMetodoPago.setSelectedIndex(0);
        }
        else{
            cmbMetodoPago.setSelectedIndex(1);
        }
        txtComprador.setText(comprador);
        txtVendedor.setText(vendedor);
        txtProductos.setText(productos);
        txtFecha.setText(fecha);
        pnl.setSelectedIndex(1);
    }
    
    
    
